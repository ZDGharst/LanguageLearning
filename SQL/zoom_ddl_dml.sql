
CREATE DATABASE zoom;
use zoom;

CREATE TABLE zoom_user (
  zoom_id int PRIMARY KEY, 
  first_name varchar(30) NOT NULL,
  last_name varchar(30) NOT NULL,
  email varchar(100) NOT NULL
  );
  
CREATE TABLE meeting (
  meeting_id int PRIMARY KEY,
  passcode int NULL, 
  meeting_name varchar(150) NOT NULL UNIQUE, 
  host_id int, 
  start_date datetime NOT NULL,
  mute_participants bit NOT NULL DEFAULT 1,
  constraint fk_meeting_host_id_user_zoom_id FOREIGN KEY (host_id) REFERENCES zoom_user(zoom_id)
  );

CREATE TABLE category (
  category_id int PRIMARY KEY, 
  category_name varchar(30) );

CREATE TABLE meeting_category (
  category_id int NOT NULL, 
  meeting_id int NOT NULL, 
  constraint pk_category_id_meeting_id PRIMARY KEY(category_id, meeting_id), 
  constraint fk_meeting_category_category_id_category_category_id FOREIGN KEY (category_id) REFERENCES category(category_id), 
  constraint fk_meeting_category_meeting_id_meeting_meeting_id FOREIGN KEY (meeting_id) REFERENCES meeting(meeting_id)
  );
  
  
CREATE TABLE calendar(
  calid int PRIMARY KEY,
  user_name varchar(50) NOT NULL UNIQUE,
  zoom_user_id int,
  constraint fk_zoom_user_id_zoom_user_zoom_id FOREIGN KEY (zoom_user_id) REFERENCES zoom_user(zoom_id)
  );

CREATE TABLE user_meeting(
  zoom_user_id int NOT NULL, 
  meeting_id int NOT NULL,
  ipv4address varchar(15) NOT NULL,
  constraint pk_user_attends PRIMARY KEY (zoom_user_id, meeting_id), 
  constraint fk_user_meeting_zoom_user_id FOREIGN KEY (zoom_user_id) REFERENCES zoom_user(zoom_id), 
  constraint fk_user_meeting_meeting_id FOREIGN KEY (meeting_id) REFERENCES meeting(meeting_id)
  );
  
  
